#include "minishell.h"

char	**is_flags(char **str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (ft_strcmp(str[i], "-") && str[i][0] == '-')
			return (&str[i]);
		i++;
	}
	return (NULL);
}
